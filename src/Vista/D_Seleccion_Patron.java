public void enlazar(){
if(!(jTextField1.getText().isEmpty())){
    
 String cod_patron=(jTextField1.getText()); 
 ResultSet rs;
 Connection con = new Conexion().getCon();
 
    try {
        rs= con.createStatement().executeQuery("select  PATRONES.cod_PATRON,colectores.nombre,DONANTE_PATRONES.NOMBRE from patrones INNER JOIN DONANTE_PATRONES ON patrones.COD_DONANTE=DONANTE_PATRONES.COD_DONANTE INNER JOIN COLECTORES ON patrones.IDENTIFICACION_COLECTOR=COLECTORES.IDENTIFICACION_COLECTOR where cod_patron= "+jTextField1.getText()+";");
        if (rs.next()) {
         
         
         Accesador.arbol_T_colector_patron.setText(rs.getString(2));
         Accesador.arbol_T_donante_patron.setText(rs.getString(3));
         Accesador.arbol_T_cod_patron.setText(jTextField1.getText());
          if (!(Accesador.arbol_T_cod_copa.getText().isEmpty())&&!(Accesador.arbol_T_cod_patron.getText().isEmpty())) {
            Accesador.arbol_T_codiigo_arbol.setText("C"+Accesador.arbol_T_cod_copa.getText()+" - "+"P"+Accesador.arbol_T_cod_patron.getText());
        }
         
        }
        
 
    } catch (SQLException ex) {
        Logger.getLogger(D_Seleccion_Donante_Patrones.class.getName()).log(Level.SEVERE, null, ex);
    }
 
 }
}
